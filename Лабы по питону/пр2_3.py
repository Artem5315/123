'''Дан неотсортированный список строк некоторых
идентификаторов в формате фамилия.имя.отчество.год рождения.день
рождения. Необходимо выполнить сортировку массива по значению
года рождения, по возрастанию.'''
# ввод неотсортированного списка идентификаторов
identifiers = []
n = int(input("Введите количество идентификаторов: "))
for i in range(n):
    identifier = input("Введите идентификатор в формате фамилия.имя.отчество.год рождения.день рождения: ")
    identifiers.append(identifier)

# функция для получения года рождения из идентификатора
def get_birth_year(identifier):
    return int(identifier.split('.')[3])

# сортировка списка по году рождения
sorted_identifiers = sorted(identifiers, key=get_birth_year)

# вывод отсортированного списка
print(sorted_identifiers)
