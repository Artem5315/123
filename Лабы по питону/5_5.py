'''Получить таблицы истинности следующих логических функций (‘*’ -
конъюнкция, ‘+’ - дизъюнкция, ‘→’ - импликация, ‘↔’ - эквивалентность, ‘mod2’ –
неравнозначность (предварительно формулы не упрощать!):'''
# Функция для вычисления значения выражения
def calc_expr(a, b, c):
    return (c + a) % 2 == b

# Вывод заголовка таблицы
print("A\tB\tC\tC+A->B mod2 A")
print("-" * 30)

# Обходим все возможные комбинации значений A, B и C
for a in range(2):
    for b in range(2):
        for c in range(2):
            # Вычисляем значение выражения
            result = calc_expr(a, b, c)
            # Выводим строку таблицы
            print(f"{a}\t{b}\t{c}\t{int(result)}")
